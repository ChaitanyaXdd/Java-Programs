// Pretty Printing
public class Strings3 {
    public static void main(String[] args) {
        System.out.println(56);
        float a = 4.4356f;
        System.out.printf("Formatted output is: %.2f",a);
        System.out.println("\n" + Math.PI);
        System.out.printf("PI: %.3f",Math.PI);
    }
}
